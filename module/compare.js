// Bigger than (>)
const e = 5 > 4;
// e = true

// Less than (<)
const f = 5 < 4;
// f = false

// Equal to (==) without type check
// Do not use this
const g = 5 == "4";

// Equal to with type check (===)
const h = 5 === 4;

// Not equal with type check (!==)
const k = 5 !== 4;

// Bigger than or equal to (>=)
const i = 5 >= 4;

// Less than or equal to (<=)
const j = 5 <= 4;
